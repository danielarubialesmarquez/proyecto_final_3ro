    private void jb_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_guardarActionPerformed
       
        
        try {
            
            con = getConexion();
            ps = con.prepareStatement("insert into trabajadores (id_empleado,nombre_em,cargo) values (?,?,?);");
            rs = ps.executeQuery();
            System.out.print("si sirvio");
        } catch (SQLException ex) {
            Logger.getLogger(trabajadores.class.getName()).log(Level.SEVERE, null, ex);
        }
             
          this.modelo.addRow(new Object[]{this.jtf_codigo.getText(),this.jtf_empleado.getText(),
            this.jcb_cargo.getSelectedItem()});
            this.jtf_codigo.setText("");
            this.jtf_empleado.setText("");
            this.jcb_cargo.setSelectedIndex(0);
            
    }//GEN-LAST:event_jb_guardarActionPerformed

