    private void jb_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_actualizarActionPerformed
      String []datos=new String[3];
      datos[0]=jtf_codigo.getText();
      datos[1]=jtf_empleado.getText();
      datos[2]=this.jcb_cargo.getSelectedItem().toString();
      for (int i = 0; i < tablaempleado.getColumnCount(); i++){
            modelo.setValueAt(datos[i], filas,1);
    }
    }//GEN-LAST:event_jb_actualizarActionPerformed

    private void jb_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_eliminarActionPerformed
         int fila_seleccionada=tablaempleado.getSelectedRow();
        if(fila_seleccionada>0){
            modelo.removeRow(fila_seleccionada);
        }else{
            JOptionPane.showMessageDialog(null,"Selecciones una fila ");
        }
    }//GEN-LAST:event_jb_eliminarActionPerformed

    private void tablaempleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaempleadoMouseClicked
        int fila_seleccionada=tablaempleado.getSelectedRow();
        jtf_codigo.setText(tablaempleado.getValueAt(fila_seleccionada,0).toString());
        jtf_empleado.setText(tablaempleado.getValueAt(fila_seleccionada,1).toString());
        jcb_cargo.setSelectedItem(tablaempleado.getValueAt(fila_seleccionada,0).toString());
        filas=fila_seleccionada;
    }//GEN-LAST:event_tablaempleadoMouseClicked

